#include <iostream>
#include <fstream>

#include "building.h"

int main(int argc, char *argv[]) {
    // -- Following was adapted from GitHub Copilot --
    if (argc != 2) {
        std::cerr << "Usage: " << argv[0] << " <building_configuration_file>" << std::endl;
        return 1;
    }

    std::ifstream inp(argv[1]);
    if (!inp.is_open()) {
        std::cerr << "Could not open file: " << argv[1] << std::endl;
        return 1;
    }

    Building building = setup_building(inp);
    if (building.num_elevators() == 0) {
        std::cerr << "No elevators found in the building configuration." << std::endl;
        return 1;
    }
    inp.close();

    std::cout << "Building has " << building.num_elevators() << " elevators." << std::endl;
    for (int i = 0; i < building.num_elevators(); i++) {
        std::cout << "Elevator " << building.elevators[i].name << ": "
                  << "Lowest floor: " << building.elevators[i].lowest << ", "
                  << "Highest floor: " << building.elevators[i].highest << ", "
                  << "Current floor: " << building.elevators[i].current << ", "
                  << "Capacity: " << building.elevators[i].capacity << std::endl;
    }
    // -- End of GitHub Copilot adaptation -- (even this comment was generated by Copilot haha)

    

    return 0;
}